def gradingStudents(grades):
    grades2=grades
    for i in range(len(grades)):
        grd=grades[i]
        if grd >= 38:
            nextMul5=int(((grd//5)+1)*5)
            if nextMul5-grd < 3:
                grades2[i]=nextMul5
                
    return grades2

